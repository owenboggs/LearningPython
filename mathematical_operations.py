# order of operation is () ** * / + -
# Parentheses
# Exponents
# Multiplication and Division (from left to right)
# Addition and Subtraction (from left to right)

print(3 * 3 + 3 / 3 - 3)
# 3 * 3 = 9
# 3 / 3 = 1
# 9 + 1 = 10
# 10 - 3 = 7

print(3 * (3 + 3) / 3 - 3)
# parentheses first
# 3 + 3 = 6
# 3 * 6 = 18
# 18 / 3 = 6
# 6 - 3 = 3
